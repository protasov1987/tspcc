    if section_data.get("screen"):
        add_image(document, screens[section_data["screen"]], section_data["title"], width=6.8)
    for image_key in section_data.get("images", []):
        add_image(document, screens[image_key], PAGE_SHOTS[image_key], width=6.8)

    document.add_heading(f"{section_prefix}.1 Назначение страницы", level=3)
    document.add_paragraph(section_data["purpose"])
    document.add_heading(f"{section_prefix}.2 Что пользователь видит и делает", level=3)
    add_bullets(document, section_data.get("features", []))
    document.add_heading(f"{section_prefix}.3 Роль страницы в общем сценарии", level=3)
    add_numbers(document, section_data.get("workflow", []))
