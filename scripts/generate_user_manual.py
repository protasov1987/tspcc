def load_app_version() -> str:
    version_file = ROOT / "app-version.json"
    with version_file.open("r", encoding="utf-8") as fh:
        raw = json.load(fh)
    stage = str(raw.get("stage") or "").strip()
    major = int(raw.get("major", 0))
    minor = int(raw.get("minor", 0))
    patch = int(raw.get("patch", 0))
    version = f"{major}.{minor:02d}.{patch:02d}"
    return f"{stage} {version}".strip()


def access_level_rows(data: dict) -> list[dict[str, str]]:
    rows: list[dict[str, str]] = []
    access_levels = data.get("accessLevels", [])
    for level in access_levels:
        permissions = level.get("permissions", {})
        tabs = permissions.get("tabs", {})
        editable = []
        readonly = []
        hidden = []
        for key, _purpose, _details in PAGE_ROWS:
            tab_key = None
            if key == "Дашборд":
                tab_key = "dashboard"
            elif key == "МК":
                tab_key = "cards"
            elif key == "Согласование":
                tab_key = "approvals"
            elif key == "Обеспечение":
                tab_key = "provision"
            elif key == "Входной контроль":
                tab_key = "input-control"
            elif key.startswith("Производство"):
                tab_key = "production"
            elif key == "Трекер":
                tab_key = "workorders"
            elif key == "Изделия":
                tab_key = "items"
            elif key == "Образцы контрольные":
                tab_key = "ok"
            elif key == "Образцы свидетели":
                tab_key = "oc"
            elif key == "Архив":
                tab_key = "archive"
            elif key == "Рабочее место":
                tab_key = "workspace"
            elif key == "Пользователи":
                tab_key = "users"
            elif key == "Уровни доступа":
                tab_key = "accessLevels"
            elif key == "Подразделения":
                tab_key = "departments"
            elif key == "Операции":
                tab_key = "operations"
            elif key == "Участки":
                tab_key = "areas"
            elif key == "Сотрудники":
                tab_key = "employees"
            elif key == "Время смен":
                tab_key = "shift-times"
            else:
                continue
