def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)


def make_placeholder(path: Path, title: str, subtitle: str) -> None:
    ensure_dir(path.parent)
    image = Image.new("RGB", (1400, 900), "#f5efe4")
    draw = ImageDraw.Draw(image)
    try:
        title_font = ImageFont.truetype("arial.ttf", 48)
        text_font = ImageFont.truetype("arial.ttf", 24)
        small_font = ImageFont.truetype("arial.ttf", 18)
    except OSError:
        title_font = ImageFont.load_default()
        text_font = ImageFont.load_default()
        small_font = ImageFont.load_default()

    draw.rounded_rectangle((60, 60, 1340, 840), radius=24, fill="#fffaf2", outline="#c48a3a", width=4)
    draw.rounded_rectangle((90, 110, 1310, 200), radius=18, fill="#153243")
    draw.text((130, 132), title, fill="#ffffff", font=title_font)
    draw.text((130, 235), subtitle, fill="#153243", font=text_font)

    left = 130
    top = 320
    colors = ["#e3d5ca", "#d5e6f2", "#e7edd8", "#f7d8ba"]
    for index in range(8):
        row = index // 2
        col = index % 2
        x1 = left + col * 560
        y1 = top + row * 120
        x2 = x1 + 500
        y2 = y1 + 70
        draw.rounded_rectangle((x1, y1, x2, y2), radius=14, fill=colors[index % len(colors)], outline="#b07d3c")
        draw.text((x1 + 24, y1 + 22), f"Экранный блок {index + 1}", fill="#2d2d2d", font=text_font)

    draw.text((130, 790), "Иллюстрация создана автоматически, если получить реальный скриншот не удалось.", fill="#5b5b5b", font=small_font)
    image.save(path)


def get_font(size: int, bold: bool = False):
    candidates = ["arialbd.ttf", "arial.ttf"] if bold else ["arial.ttf", "segoeui.ttf"]
    for candidate in candidates:
        try:
            return ImageFont.truetype(candidate, size)
        except OSError:
            continue
    return ImageFont.load_default()


def draw_text_block(draw: ImageDraw.ImageDraw, text: str, xy: tuple[int, int], font, fill: str, width: int) -> None:
    wrapped = textwrap.fill(text, width=width)
    draw.multiline_text(xy, wrapped, font=font, fill=fill, spacing=6)


def draw_box(draw: ImageDraw.ImageDraw, box: tuple[int, int, int, int], title: str, body: str, fill: str, outline: str) -> None:
    draw.rounded_rectangle(box, radius=18, fill=fill, outline=outline, width=3)
    title_font = get_font(28, bold=True)
    text_font = get_font(18)
    x1, y1, x2, y2 = box
    draw.text((x1 + 18, y1 + 14), title, font=title_font, fill="#173042")
    draw_text_block(draw, body, (x1 + 18, y1 + 58), text_font, "#173042", width=28)


def draw_arrow(draw: ImageDraw.ImageDraw, start: tuple[int, int], end: tuple[int, int], fill: str = "#173042", width: int = 5) -> None:
    draw.line([start, end], fill=fill, width=width)
    ex, ey = end
    draw.polygon([(ex, ey), (ex - 16, ey - 8), (ex - 16, ey + 8)], fill=fill)


