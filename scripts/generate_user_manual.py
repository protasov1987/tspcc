def save_document_with_fallback(document: Document) -> Path:
    try:
        document.save(OUTPUT_DOCX)
        return OUTPUT_DOCX
    except PermissionError:
        fallback = DOCS_DIR / f"user-manual-tspcc-{datetime.now().strftime('%Y%m%d-%H%M%S')}.docx"
        document.save(fallback)
        return fallback


